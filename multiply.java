public class MultiplyTwoNumbers {
    public static void main(String[] args) {
        // Define two numbers to multiply
        int num1 = 5;
        int num2 = 7;

        // Calculate the product
        int product = num1 * num2;

        // Display the result
        System.out.println("The product of " + num1 + " and " + num2 + " is: " + product);
    }
}
